package bmatser.model;

public class FreightAmount {
	
	private String dealerId;
	private Double defaultExpressPrice;
	private String byintervallocation;
	
	
	public String getDealerId() {
		return dealerId;
	}
	public void setDealerId(String dealerId) {
		this.dealerId = dealerId;
	}
	public Double getDefaultExpressPrice() {
		return defaultExpressPrice;
	}
	public void setDefaultExpressPrice(Double defaultExpressPrice) {
		this.defaultExpressPrice = defaultExpressPrice;
	}
	public String getByintervallocation() {
		return byintervallocation;
	}
	public void setByintervallocation(String byintervallocation) {
		this.byintervallocation = byintervallocation;
	}
	
	
}
